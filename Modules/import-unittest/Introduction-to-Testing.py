# ### Best Practices ###

# 1. Test should be isolated i.e. our test shouldn't rely on other tests or effects of other tests so we should be able to run any tests by itself independent of the other tests
# 2. We can add tests to existing code but there is something called test-driven development and basically it means that you write the test before you write the code 

# test-driven development concept - We should think about what we want your code to do and then write a test implementing that behaviour then watch the test fail since it doesn't actually have any code to run against and then to write the code in a way that it gets the test to passed

# Any testing is better then no testing

